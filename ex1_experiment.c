#include <stdio.h>

/* This is a comment. */
int main(int argc, char *argv[])
{
    int distance = 100;

    // this is also a comment
    printf("You are %d miles away.\n", distance);
    printf("Hello world\n");
    printf("My name is %s\n", "Jackie");
    printf("\tEven more text without newline");
    printf("This is the end\n");

    return 0;
}
